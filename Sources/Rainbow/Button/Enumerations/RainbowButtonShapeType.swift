import SwiftUI

/// An enumeration that defines the shape types for a Rainbow button.
public enum RainbowButtonShapeType: Equatable {
    /// A circular button shape.
    case circle
    
    /// A rounded rectangle button shape.
    case roundedRectangle
    
    /// A rectangular button shape.
    case rectangle
    
    /// A capsule-shaped button.
    case capsule
}

