import Foundation

/**
 An enumeration representing the different types of loaders that can be used in the Rainbow UI library.
 */
public enum RainbowLoaderType {
    /**
     A rainbow-colored loader.
     */
    case rainbow
    
    /**
     A pulsating loader.
     */
    case pulsating
    
    /**
     A rotating square loader.
     */
    case rotatingSquare
    
    /**
     A dots loader.
     */
    case dots
    
    /**
     A progress loader.
     */
    case progress
}
